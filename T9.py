def correct_text(input_text, dictionary):
    def distance(a, b):
        n, m = len(a), len(b)
        if n == 0:
            return m
        if m == 0:
            return n

        if n > m:
            a, b = b, a
            n, m = m, n

        current_row = range(n + 1)
        for i in range(1, m + 1):
            previous_row, current_row = current_row, [i] + [0] * n
            for j in range(1, n + 1):
                add = previous_row[j] + 1
                delete = current_row[j - 1] + 1
                change = previous_row[j - 1]
                if a[j - 1] != b[i - 1]:
                    change += 1
                current_row[j] = min(add, delete, change)

        return current_row[n]

    words = input_text.split()
    corrected_words = []

    for word in words:
        # Находим наиболее близкое слово из словаря
        closest_word = min(dictionary, key=lambda dict_word: distance(word.lower(), dict_word.lower()))
        dist = distance(word.lower(), closest_word.lower())

        # Если расстояние меньше или равно 2, заменяем слово
        if dist <= 2:
            corrected_words.append(closest_word)
        else:
            corrected_words.append(word)

    return ' '.join(corrected_words)


